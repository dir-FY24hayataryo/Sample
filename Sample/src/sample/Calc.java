package sample;

public class Calc {

    public static void main(String[] args) {
        var n1=100;
        var n2=10;
        
        var result1=n1+n2;
        System.out.println(result1);
        
        var result2=n1-n2;
        System.out.println(result2);
        
        var result3=n1*n2;
        System.out.println(result3);
        
        var result4=n1/n2;
        System.out.println(result4);
        
        var result5=n1%n2;
        System.out.println(result5);

    }

}
